package strings;

public class Application {
	public static void main(String[] args) {
		int myInt = 7;
		
		String text = "Hello";
		
		String blank = " ";
		
		String name = "Bob";
		
		String greeting = text + blank + name;
		
		System.out.println(greeting);
		
		System.out.println("My integer is: " + myInt);
		
		double myDouble = 7.2;
		
		System.out.println("My integer is: "+ myDouble + ".");
	}
}
